'use strict';
// Emit quest-overlay.js (a browser-loadable `const questOverlayData = {…}`) from
// the validated quest-overlay.json. The app is zero-build and runs from file://,
// where fetch() of a .json is blocked — so structured quest attributes reach the
// client the same way cards do: as a global defined in a plain <script>.
//
//   node scripts/build-quest-overlay-js.js
//
// Re-run whenever quest-overlay.json changes (e.g. after curating more quests).
// The JSON stays the source of truth (validated by validate-data.js); this file
// is generated output.

const fs = require('fs');
const path = require('path');
const { ROOT } = require('./lib');

const jsonPath = path.join(ROOT, 'quest-overlay.json');
if (!fs.existsSync(jsonPath)) {
  console.error('quest-overlay.json not found.');
  process.exit(1);
}
const data = JSON.parse(fs.readFileSync(jsonPath, 'utf8'));
const banner = '// GENERATED by scripts/build-quest-overlay-js.js from quest-overlay.json — do not edit by hand.\n';
const body = banner + 'const questOverlayData = ' + JSON.stringify(data, null, 2) + ';\n';
fs.writeFileSync(path.join(ROOT, 'quest-overlay.js'), body);
console.log(`Wrote quest-overlay.js: ${Object.keys(data).length} quest(s).`);
